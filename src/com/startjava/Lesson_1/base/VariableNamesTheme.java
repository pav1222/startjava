package com.startjava.Lesson_1.base;

public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("\n1. Разные " + "переменные: ");

        // цифра
        int digit = 1;
        System.out.println("цифра = " + digit);

        // сумма чисел
        int sumNumbers = 22;
        System.out.println("сумма чисел = " + sumNumbers);

        // произведение цифр
        int productDigits = 132;
        System.out.println("произведение цифр = " + productDigits);

        // макcимальное число
        int max = 200;
        System.out.println("макс. число = " + max);

        // количество десятков
        int quantityTens = 2;
        System.out.println("количество десятков = " + quantityTens);

        // вес собаки
        double dogWeight = 7.5;
        System.out.println("вес собаки = " + dogWeight);

        // исходное число
        double originalNumber = 2.3;
        System.out.println("исходное число = " + originalNumber);

        // процент по вкладу
        int rate = 23;
        System.out.println("процент по вкладу = " + rate + "%");

        // код ошибки
        int errorCode = 404;
        System.out.println("код ошибки = " + errorCode);

        // тип сообщения
        String messageType = "MMS";
        System.out.println("тип сообщения = " + messageType);

        // число нулей
        int numberZeros = 10;
        System.out.println("число нулей = " + numberZeros);

        // уникальное число
        double uniqueNumber = 12.5;
        System.out.println("уникальное число = " + uniqueNumber);

        // случайное число
        int randomNumber = 320;
        System.out.println("случайное число = " + randomNumber);

        // математическое выражение
        String mathExpression = "2+2";
        System.out.println("мат. выражение = " + mathExpression);

        // выбор
        String choise = "Да или нет?";
        System.out.println("твой выбор = " + choise);

        // счет
        int score = 22;
        System.out.println("счет = " + score);

        // максимальная длина
        int maxLength = 120;
        System.out.println("макс. длина = " + maxLength);

        // пункт меню
        byte menuItem = 3;
        System.out.println("пункт меню = " + menuItem);

        // стоимость кофе на вынос
        float coffeePriceToGo = 120.2f;
        System.out.println("стоимость кофе на вынос = " + coffeePriceToGo);

        // дата начала
        String startDate = "12.05.2029";
        System.out.println("дата начала = " + startDate);

        // окончание диапазона
        int endRange = 45;
        System.out.println("Окончание диапазона = " + endRange);

        // полное имя
        String fullNameEmployeeMonth = "Александр Александрович Александров";
        System.out.println("полное имя = " + fullNameEmployeeMonth);

        // заголовок электронной книги
        String ebookTitle = "Alisa";
        System.out.println("заголовок этой книги = " + ebookTitle);

        // размер
        long size = 12000000l;
        System.out.println("размер = " + size);

        // вместимость
        long capacity = 25000000l;
        System.out.println("вместимость = " + capacity);

        // счетчик
        short counter = 13;
        System.out.println("в данный момент = " + counter);

        // путь до файла
        String filePath = "С:/Program Files/";
        System.out.println("путь до файла = " + filePath);

        // количество чисел в каждой строке
        int numbersInRow = 125;
        System.out.println("сколько чисел в каждой строке = " + numbersInRow);
        System.out.println();

        System.out.println("\n\n2. boolean-переменные\n\n");
        // сотни равны?
        boolean isEqualHundreds = true;
        System.out.println("равны ли сотни = " + isEqualHundreds);

        // компьютер включен?
        boolean isComputerOn = false;
        System.out.println("включен ли компьютер? = " + isComputerOn);

        // есть равные цифры?
        boolean hasEqualNums = true;
        System.out.println("есть ли равные цифры? = " + hasEqualNums);

        // (что-либо ) создано?
        boolean isCreated = false;
        System.out.println("что-то создано? = " + isCreated);

        // (что-либо ) пустое?
        boolean isEmpty = true;
        System.out.println("пусто ли? = " + isEmpty);

        // (что-либо ) активное?
        boolean isActive = false;
        System.out.println("активно ли? = " + isActive);

        // новый?
        boolean isNew = true;
        System.out.println("новый ли? = " + isNew);

        // электронная почта действительна?
        boolean isValidEmail = false;
        System.out.println("действительность e-mail = " + isValidEmail);

        // имеются уникальные строки?
        boolean hasUniqueStrings = true;
        System.out.println("наличие уникальных строк =  " + hasUniqueStrings);
        System.out.println();

        System.out.println("\n\n3. Аббревиатуры\n\n");
        // старый universally unique identifier
        String oldUuid = "00332277-6541-7781-1234-alkbcafdegxx";
        System.out.println("Старый UUID = " + oldUuid);

        // производитель оперативной памяти
        String ramManufacturer = "ADATA";
        System.out.println("Производитель ram = " + ramManufacturer);

        // емкость жесткого диска
        int hddCapacity = 2200000;
        System.out.println("Емкость HDD = " + hddCapacity + "Мб");

        // протокол передачи гипертекста
        String http = "HTTP";
        System.out.println("Протокол передачи гипертекста = " + http);

        // сокращенный uniform resource locator
        String shortUrl = "http://short.url/175";
        System.out.println("Сокращенный URL = " + shortUrl);

        // новый идентификатор клиента
        int newClientId = 185869696;
        System.out.println("Новый идентификатор клиента = " + newClientId);

        // кодировка american standard code for information interchange
        String ascii = "ASCII";
        System.out.println("Кодировка = " + ascii);
    } 
}





